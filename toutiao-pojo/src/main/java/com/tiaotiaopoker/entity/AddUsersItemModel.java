package com.tiaotiaopoker.entity;

public class AddUsersItemModel {
    private String name;
    private String phone;
    private String idCard;
    private int sign;
    private String partnerName;
    private String partnerPhone;
    private String partnerIdCard;
    private int partnerSign;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getIdCard() {
        return idCard;
    }

    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    public int getSign() {
        return sign;
    }

    public void setSign(int sign) {
        this.sign = sign;
    }

    public String getPartnerName() {
        return partnerName;
    }

    public void setPartnerName(String partnerName) {
        this.partnerName = partnerName;
    }

    public String getPartnerPhone() {
        return partnerPhone;
    }

    public void setPartnerPhone(String partnerPhone) {
        this.partnerPhone = partnerPhone;
    }

    public String getPartnerIdCard() {
        return partnerIdCard;
    }

    public void setPartnerIdCard(String partnerIdCard) {
        this.partnerIdCard = partnerIdCard;
    }

    public int getPartnerSign() {
        return partnerSign;
    }

    public void setPartnerSign(int partnerSign) {
        this.partnerSign = partnerSign;
    }
}
